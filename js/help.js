function showHelp(help) {
    if (help == 0) {
        alert("Home");
    } else if (help == 1) {
        alert("New User");
    } else if (help == 2) {
        alert("Existing User");
    } else if (help == 3) {
        alert("Main Menu");
    } else if (help == 4) {
        alert("During Quiz");
    } else if (help == 5) {
        alert("Last Questin");
    } else if (help == 6) {
        alert("Answers Displayed");
    } else if (help == 7) {
        alert("Leaderboard");
    }
}