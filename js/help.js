function showHelp(help) {
    if (help == 0) {
        alert("Choose either New User of Existing User");
    } else if (help == 1) {
        alert("Please Enter your Forname and Surname and click 'Enter with New User' to proceed");
    } else if (help == 2) {
        alert("You can search for your name using the search box, and click Select User to proceed");
    } else if (help == 3) {
        alert("Choose an Option from one off the below butttons");
    } else if (help == 4) {
        alert("Please select your answer by clicking on the box beside answer, click 'Next Question' to proceed");
    } else if (help == 5) {
        alert("Please select your answer by clicking on the box beside answer, click 'Finish Quiz' to proceed");
    } else if (help == 6) {
        alert("If you wish you may view leaderboard or return to Quiz Main menu, using the above buttons");
    } else if (help == 7) {
        alert("You may search the leaderboard, or return to Main Menu using button");
    }
}